#define VOID(p)		((const void*)(p))

static void PrintString(const TString* ts)
{
 const char* s=getstr(ts);
 size_t i,n=ts->tsv.len;
 printf("%c",'"');
 for (i=0; i<n; i++)
 {
  int c=(int)(unsigned char)s[i];
  switch (c)
  {
   case '"':  printf("\\\""); break;
   case '\\': printf("\\\\"); break;
   case '\a': printf("\\a"); break;
   case '\b': printf("\\b"); break;
   case '\f': printf("\\f"); break;
   case '\n': printf("\\n"); break;
   case '\r': printf("\\r"); break;
   case '\t': printf("\\t"); break;
   case '\v': printf("\\v"); break;
   default:	if (isprint(c))
   			printf("%c",c);
		else
			printf("\\%03d",c);
  }
 }
 printf("%c",'"');
}

static void PrintConstant(const Proto* f, int i)
{
 const TValue* o=&f->k[i];
 switch (ttype(o))
 {
  case LUA_TNIL:
	printf("nil");
	break;
  case LUA_TBOOLEAN:
	printf(bvalue(o) ? "true" : "false");
	break;
  case LUA_TNUMBER:
	printf(LUA_NUMBER_FMT,nvalue(o));
	break;
  case LUA_TSTRING:
	PrintString(rawtsvalue(o));
	break;
  default:				/* cannot happen */
	printf("? type=%d",ttype(o));
	break;
 }
}

#define UPVALNAME(x) ((f->upvalues[x].name) ? getstr(f->upvalues[x].name) : "-")
#define MYK(x)		(-1-(x))

static void PrintCode(const Proto* f)
{
 const Instruction* code=f->code;
 int pc,n=f->sizecode;
 for (pc=0; pc<n; pc++)
 {
  Instruction i=code[pc];
  OpCode o=GET_OPCODE(i);
  int a=GETARG_A(i);
  int b=GETARG_B(i);
  int c=GETARG_C(i);
  int ax=GETARG_Ax(i);
  int bx=GETARG_Bx(i);
  int sbx=GETARG_sBx(i);
  int line=getfuncline(f,pc);
  printf("\t%d\t",pc+1);
  if (line>0) printf("[%d]\t",line); else printf("[-]\t");
  printf("%-9s\t",luaP_opnames[o]);
  switch (getOpMode(o))
  {
   case iABC:
    printf("%d",a);
    if (getBMode(o)!=OpArgN) printf(" %d",ISK(b) ? (MYK(INDEXK(b))) : b);
    if (getCMode(o)!=OpArgN) printf(" %d",ISK(c) ? (MYK(INDEXK(c))) : c);
    break;
   case iABx:
    printf("%d",a);
    if (getBMode(o)==OpArgK) printf(" %d",MYK(bx));
    if (getBMode(o)==OpArgU) printf(" %d",bx);
    break;
   case iAsBx:
    printf("%d %d",a,sbx);
    break;
   case iAx:
    printf("%d",MYK(ax));
    break;
  }
  switch (o)
  {
   case OP_LOADK:
    printf("\t; "); PrintConstant(f,bx);
    break;
   case OP_GETUPVAL:
   case OP_SETUPVAL:
    printf("\t; %s",UPVALNAME(b));
    break;
   case OP_GETTABUP:
    printf("\t; %s",UPVALNAME(b));
    if (ISK(c)) { printf(" "); PrintConstant(f,INDEXK(c)); }
    break;
   case OP_SETTABUP:
    printf("\t; %s",UPVALNAME(a));
    if (ISK(b)) { printf(" "); PrintConstant(f,INDEXK(b)); }
    if (ISK(c)) { printf(" "); PrintConstant(f,INDEXK(c)); }
    break;
   case OP_GETTABLE:
   case OP_SELF:
    if (ISK(c)) { printf("\t; "); PrintConstant(f,INDEXK(c)); }
    break;
   case OP_SETTABLE:
   case OP_ADD:
   case OP_SUB:
   case OP_MUL:
   case OP_DIV:
   case OP_POW:
   case OP_EQ:
   case OP_LT:
   case OP_LE:
    if (ISK(b) || ISK(c))
    {
     printf("\t; ");
     if (ISK(b)) PrintConstant(f,INDEXK(b)); else printf("-");
     printf(" ");
     if (ISK(c)) PrintConstant(f,INDEXK(c)); else printf("-");
    }
    break;
   case OP_JMP:
   case OP_FORLOOP:
   case OP_FORPREP:
   case OP_TFORLOOP:
    printf("\t; to %d",sbx+pc+2);
    break;
   case OP_CLOSURE:
    printf("\t; %p",VOID(f->p[bx]));
    break;
   case OP_SETLIST:
    if (c==0) printf("\t; %d",(int)code[++pc]); else printf("\t; %d",c);
    break;
   case OP_EXTRAARG:
    printf("\t; "); PrintConstant(f,ax);
    break;
   default:
    break;
  }
  printf("\n");
 }
}

#define SS(x)	((x==1)?"":"s")
#define S(x)	(int)(x),SS(x)

static void PrintHeader(const Proto* f)
{
 const char* s=f->source ? getstr(f->source) : "=?";
 if (*s=='@' || *s=='=')
  s++;
 else if (*s==LUA_SIGNATURE[0])
  s="(bstring)";
 else
  s="(string)";
 printf("\n%s <%s:%d,%d> (%d instruction%s at %p)\n",
 	(f->linedefined==0)?"main":"function",s,
	f->linedefined,f->lastlinedefined,
	S(f->sizecode),VOID(f));
 printf("%d%s param%s, %d slot%s, %d upvalue%s, ",
	(int)(f->numparams),f->is_vararg?"+":"",SS(f->numparams),
	S(f->maxstacksize),S(f->sizeupvalues));
 printf("%d local%s, %d constant%s, %d function%s\n",
	S(f->sizelocvars),S(f->sizek),S(f->sizep));
}

static void PrintDebug(const Proto* f)
{
 int i,n;
 n=f->sizek;
 printf("constants (%d) for %p:\n",n,VOID(f));
 for (i=0; i<n; i++)
 {
  printf("\t%d\t",i+1);
  PrintConstant(f,i);
  printf("\n");
 }
 n=f->sizelocvars;
 printf("locals (%d) for %p:\n",n,VOID(f));
 for (i=0; i<n; i++)
 {
  printf("\t%d\t%s\t%d\t%d\n",
  i,getstr(f->locvars[i].varname),f->locvars[i].startpc+1,f->locvars[i].endpc+1);
 }
 n=f->sizeupvalues;
 printf("upvalues (%d) for %p:\n",n,VOID(f));
 for (i=0; i<n; i++)
 {
  printf("\t%d\t%s\t%d\t%d\n",
  i,UPVALNAME(i),f->upvalues[i].instack,f->upvalues[i].idx);
 }
}



static void SexeDumpHeader(DumpState* D)
{
 lu_byte h[LUAC_HEADERSIZE];
 sexe_header(h);
 DumpBlock(h,LUAC_HEADERSIZE,D);
}

static void SexeDumpConstants(const Proto* f, DumpState* D)
{
  sexe_stack_t stack;
  sexe_const_t con;
  shbuf_t *buff;
  char *str;
  int str_idx;
  int i,n=f->sizek;

  /* literal strings */
  memset(&stack, 0, sizeof(stack));
  stack.type = SESTACK_LITERAL;
  buff = shbuf_init();
  for (i=0; i<n; i++) {
    const TValue* o=&f->k[i];
    if (ttype(o) != LUA_TSTRING)
      continue;
    DumpString(rawtsvalue(o),D);
    str = rawtsvalue(o);
    shbuf_cat(buff, str, strlen(str) + 1);
  }
  stack.size = shbuf_size(buff);
  DumpBlock(&stack, sizeof(stack), D);
  DumpBlock(shbuf_data(buff), shbuf_size(buff), D);
  shbuf_free(&buff);

  /* constants */
  str_idx = 0;
  memset(&stack, 0, sizeof(stack));
  stack.type = SESTACK_CONSTANT;
  stack.size = f->sizek;
  DumpBlock(&stack, sizeof(stack), D);
  for (i=0; i < f->sizek; i++) {
    const TValue* o=&f->k[i];

    memset(&con, 0, sizeof(con));
    switch (ttype(o))
    {
      case LUA_TNIL:
        con.con_type = SECON_NIL;
        break;
      case LUA_TBOOLEAN:
        con.con_type = SECON_BOOL;
        con.con_val = (uint64_t)bvalue(o); 
        break;
      case LUA_TNUMBER:
        con.con_type = SECON_NUMBER;
        con.con_val = (uint64_t)nvalue(o);
        break;
      case LUA_TSTRING:
        con.con_type = SECON_LITERAL;
        con.con_val = (uint64_t)str_idx;
        str_idx++;
        break;
    }
    DumpBlock(&con, sizeof(con), D);
  }

  n=f->sizep;
  DumpInt(n,D);
  for (i=0; i<n; i++) DumpFunction(f->p[i],D);
}

static int SexeDumpUpvalues(const Proto* f, DumpState* D)
{
  sexe_stack_t *stack;
  sexe_upval_t *upv;
  int len;
  int i;

  len = sizeof(sexe_stack_t) + (f->sizeupvalues * sizeof(sexe_upval_t));
  stack = (sexe_stack_t *)calloc(1, len);
  if (!stack)
    return (SHERR_NOMEM);

  stack->type = SESTACK_UPVAL;
  stack->size = f->sizeupvalues; 
  upv = (sexe_upval_t *)stack->stack.upv;
  for (i = 0; i < f->sizeupvalues; i++) {
    upv[i].upv_instack = f->upvalues[i].instack;
    upv[i].upv_idx = f->upvalues[i].idx;
  }
  DumpBlock(stack, len, D);

  free(stack);

  return (0);
}

static int SexeDumpCode(const Proto *f, DumpState *D)
{
  sexe_stack_t *stack;
  uint32_t *instr;
  int len;
  int i;

  len = sizeof(sexe_stack_t) + (f->sizecode * sizeof(uint32_t));
  stack = (sexe_stack_t *)calloc(1, len);
  if (!stack)
    return (SHERR_NOMEM);

  instr = (uint32_t *)stack->stack.instr;
  for (i = 0; i < f->sizecode; i++)
    instr[i] = f->code[i]; 
  DumpBlock(stack, len, D);

  free(stack);

  return (0);
}

static void SexeDumpFunction(const Proto* f, DumpState* D)
{
  sexe_func_t func;
  int i;

  /* primary fuction info */
  memset(&func, 0, sizeof(func));
//  func.stack.type = SESTACK_FUNCTION;
  func.func_line = f->linedefined;
  func.func_lline = f->lastlinedefined;
  func.param_max = f->numparams;
  func.stack_max = f->maxstacksize;
  DumpBlock(&func, sizeof(func), D);

  SexeDumpCode(f, D);
  SexeDumpConstants(f, D);

  /* dump sub-level of functions */
  for (i = 0; i < f->sizep; i++) {
    SexeDumpFunction(f->p[i], D);
  }

  SexeDumpUpvalues(f,D);

}




int sexe_bcode_write(lua_State* L, const Proto* f, lua_Writer w, void* data, int strip)
{
  DumpState D;

  memset(&D, 0, sizeof(D));
  D.L = L;
  D.writer = w;
  D.data = data;
  D.strip = strip;
  D.status = 0;
  SexeDumpHeader(&D);
  SexeDumpFunction(f, &D);

  return (0);
}

